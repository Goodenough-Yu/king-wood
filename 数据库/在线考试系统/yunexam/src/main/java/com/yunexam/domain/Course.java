package com.yunexam.domain;

public class Course {
    private int cid; // 课程id
    private String cname; // 课程名称

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }
}
